function fn_add(classid,teachid,iday,icourse)
{
	$('#classid').val(classid);
	$('#teachid').val(teachid);
	$('#iday').val(iday);
	$('#icourse').val(icourse);
	$('#btnsave').click();
}
function fn_del(classid,teachid,iday,icourse)
{
	$('#classid').val(classid);
	$('#teachid').val(teachid);
	$('#iday').val(iday);
	$('#icourse').val(icourse);
	$('#btndel').click();
}